

    fn f(x: f64) -> f64 { // интегрируемая функция
        x
    }

    fn main() {
        let a = 0.; // левый конец интервала
        let b = 1.; // правый конец интервала
        let n = 100000000; // количество точек разбиения
        let h = (b - a) / (n as f64); // шаг интегрирования
        let mut res = 0.5 * (f(a) + f(b)) * h;
        for i in 1..n {
            res += f(a + (i as f64) * h) * h;
        }
        println!("Интеграл от {} до {} = {}", a, b, res);
    }
